package com.mks.luoojbackendquestionservice.controller;

import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.google.gson.Gson;
import com.mks.luoojbackendcommon.annotation.AuthCheck;
import com.mks.luoojbackendcommon.common.BaseResponse;
import com.mks.luoojbackendcommon.common.DeleteRequest;
import com.mks.luoojbackendcommon.common.ErrorCode;
import com.mks.luoojbackendcommon.common.ResultUtils;
import com.mks.luoojbackendcommon.constant.UserConstant;
import com.mks.luoojbackendcommon.exception.BusinessException;
import com.mks.luoojbackendcommon.exception.ThrowUtils;
import com.mks.luoojbackendmodel.model.dto.question.*;
import com.mks.luoojbackendmodel.model.dto.questionsubmit.QuestionSubmitAddRequest;
import com.mks.luoojbackendmodel.model.dto.questionsubmit.QuestionSubmitQueryRequest;
import com.mks.luoojbackendmodel.model.entity.Question;
import com.mks.luoojbackendmodel.model.entity.QuestionSubmit;
import com.mks.luoojbackendmodel.model.entity.User;
